package com.nopackname.bean;

public class Gift {
    private int id;
    private String msg;
    private int s;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getS() {
        return s;
    }

    public void setS(int s) {
        this.s = s;
    }

}
